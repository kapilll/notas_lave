def get_strategy_performance() -> list[dict]:
    """Analyze which strategies contribute most to winning trades."""
    db = get_db()
    trades = db.query(TradeLog).filter(TradeLog.exit_price.isnot(None)).all()

    strategy_stats: dict[str, dict] = {}
    for t in trades:
        if not t.strategies_agreed:
            continue
        strategies = json.loads(t.strategies_agreed)
        for s in strategies:
            if s not in strategy_stats:
                strategy_stats[s] = {"wins": 0, "losses": 0, "total_pnl": 0.0}
            if t.pnl and t.pnl > 0:
                strategy_stats[s]["wins"] += 1
            else:
                strategy_stats[s]["losses"] += 1
            strategy_stats[s]["total_pnl"] += t.pnl or 0

    return [
        {
            "strategy": name,
            "wins": stats["wins"],
            "losses": stats["losses"],
            "total_trades": stats["wins"] + stats["losses"],
            "win_rate": stats["wins"] / max(stats["wins"] + stats["losses"], 1) * 100,
            "total_pnl": round(stats["total_pnl"], 2),
        }
        for name, stats in sorted(strategy_stats.items(), key=lambda x: x[1]["total_pnl"], reverse=True)
    ]


def save_risk_state(starting_balance: float, current_balance: float, total_pnl: float, peak_balance: float):
    """Save risk manager state to DB so it survives restarts (Fix #8)."""
    db = get_db()
    state = db.query(RiskState).first()
    if state:
        state.starting_balance = starting_balance
        state.current_balance = current_balance
        state.total_pnl = total_pnl
        state.peak_balance = peak_balance
        state.updated_at = datetime.now(timezone.utc)
    else:
        db.add(RiskState(
            starting_balance=starting_balance,
            current_balance=current_balance,
            total_pnl=total_pnl,
            peak_balance=peak_balance,
        ))
    db.commit()


def load_risk_state() -> dict | None:
    """Load risk state from DB on startup (Fix #8)."""
    db = get_db()
    state = db.query(RiskState).first()
    if state:
        return {
            "starting_balance": state.starting_balance,
            "current_balance": state.current_balance,
            "total_pnl": state.total_pnl,
            "peak_balance": state.peak_balance,
        }
    return None


def checkpoint_wal():
    """Run WAL checkpoint to keep WAL file size small.
    Should be called periodically (e.g., hourly).
    """
    for key, engine in list(_engines.items()):
        try:
            with engine.connect() as conn:
                conn.exec_driver_sql("PRAGMA wal_checkpoint(TRUNCATE)")
                conn.commit()
        except Exception:
            pass


def _get_db_path(db_key: str) -> str | None:
    """Get the filesystem path for a database engine."""
    engine = _engines.get(db_key)
    if engine is None:
        return None
    url = str(engine.url)
    # SQLite URLs look like: sqlite:///path/to/db.file
    if url.startswith("sqlite:///"):
        return url.replace("sqlite:///", "", 1)
    return None


def backup_database(db_key: str = "lab"):
    """Create a backup copy of the database file.
    Saves to engine/data/backups/ with date suffix.
    Keeps last 7 backups.
    """
    db_path = _get_db_path(db_key)
    if db_path is None or not os.path.exists(db_path):
        return

    # Determine backup directory: engine/data/backups/
    engine_dir = Path(__file__).resolve().parent.parent.parent  # engine/
    backup_dir = engine_dir / "data" / "backups"
    backup_dir.mkdir(parents=True, exist_ok=True)

    # Copy with date suffix
    db_name = Path(db_path).stem
    date_str = datetime.now().strftime("%Y%m%d")
    dest = backup_dir / f"{db_name}_{date_str}.db"
    shutil.copy2(db_path, dest)

    # Delete backups older than 7 days
    cutoff = datetime.now() - timedelta(days=7)
    for backup_file in sorted(backup_dir.glob(f"{db_name}_*.db")):
        try:
            mtime = datetime.fromtimestamp(backup_file.stat().st_mtime)
            if mtime < cutoff:
                backup_file.unlink()
        except OSError:
            pass


def run_db_maintenance():
    """Run periodic DB maintenance: checkpoint WAL + backup."""
    checkpoint_wal()
    backup_database("lab")
    backup_database("default")
