def get_strategy_performance() -> list[dict]:
    """Analyze which strategies contribute most to winning trades."""
    db = get_db()
    trades = db.query(TradeLog).filter(TradeLog.exit_price.isnot(None)).all()

    strategy_stats: dict[str, dict] = {}
    for t in trades:
        if not t.strategies_agreed:
            continue
        strategies = json.loads(t.strategies_agreed)
        for s in strategies:
            if s not in strategy_stats:
                strategy_stats[s] = {"wins": 0, "losses": 0, "total_pnl": 0.0}
            if t.pnl and t.pnl > 0:
                strategy_stats[s]["wins"] += 1
            else:
                strategy_stats[s]["losses"] += 1
            strategy_stats[s]["total_pnl"] += t.pnl or 0

    return [
        {
            "strategy": name,
            "wins": stats["wins"],
            "losses": stats["losses"],
            "total_trades": stats["wins"] + stats["losses"],
            "win_rate": stats["wins"] / max(stats["wins"] + stats["losses"], 1) * 100,
            "total_pnl": round(stats["total_pnl"], 2),
        }
        for name, stats in sorted(strategy_stats.items(), key=lambda x: x[1]["total_pnl"], reverse=True)
    ]


def save_risk_state(starting_balance: float, current_balance: float, total_pnl: float, peak_balance: float):
    """Save risk manager state to DB so it survives restarts (Fix #8)."""
    db = get_db()
    state = db.query(RiskState).first()
    if state:
        state.starting_balance = starting_balance
        state.current_balance = current_balance
        state.total_pnl = total_pnl
        state.peak_balance = peak_balance
        state.updated_at = datetime.now(timezone.utc)
    else:
        db.add(RiskState(
            starting_balance=starting_balance,
            current_balance=current_balance,
            total_pnl=total_pnl,
            peak_balance=peak_balance,
        ))
    db.commit()


def load_risk_state() -> dict | None:
    """Load risk state from DB on startup (Fix #8)."""
    db = get_db()
    state = db.query(RiskState).first()
    if state:
        return {
            "starting_balance": state.starting_balance,
            "current_balance": state.current_balance,
            "total_pnl": state.total_pnl,
            "peak_balance": state.peak_balance,
        }
    return None
