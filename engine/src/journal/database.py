"""
Trade Journal — SQLite database for logging every signal, evaluation, and trade.

This is the foundation of the learning engine. Every trade gets logged with:
- All strategy signals at the time of entry
- Claude's reasoning and confidence
- Market regime
- Actual outcome (P&L, duration, max adverse/favorable excursion)
- Post-trade analysis

This data feeds the learning engine:
- Which strategies perform best per instrument?
- Which regime produces the most winners?
- What's the optimal confluence score threshold?
- Are there time-of-day patterns?
"""

import json
from datetime import datetime, timezone
from contextlib import contextmanager
from sqlalchemy import (
    create_engine, Column, String, Float, Integer, Boolean, Text, DateTime,
    MetaData, Table, Index, ForeignKey, event
)
from sqlalchemy.orm import DeclarativeBase, Session, sessionmaker, scoped_session
from ..config import config


class Base(DeclarativeBase):
    pass


class SignalLog(Base):
    """Every signal generated by the system, even if no trade was taken."""
    __tablename__ = "signal_logs"

    id = Column(Integer, primary_key=True, autoincrement=True)
    timestamp = Column(DateTime, default=lambda: datetime.now(timezone.utc))
    symbol = Column(String(20), nullable=False)
    timeframe = Column(String(10), nullable=False)
    regime = Column(String(20))
    composite_score = Column(Float, default=0.0)
    direction = Column(String(10))  # LONG, SHORT, or null
    agreeing_strategies = Column(Integer, default=0)
    total_strategies = Column(Integer, default=0)
    # JSON blob of all strategy signals
    signals_json = Column(Text)
    # Claude's decision
    claude_action = Column(String(10))  # BUY, SELL, SKIP
    claude_confidence = Column(Integer, default=0)
    claude_reasoning = Column(Text)
    # Risk manager result
    risk_passed = Column(Boolean, default=False)
    risk_rejections = Column(Text)  # JSON list
    # DE-03: Data lineage — trace signal back to the specific candle
    candle_timestamp = Column(DateTime)   # Timestamp of the candle that triggered the signal
    candle_close = Column(Float)          # Close price of the triggering candle
    # Final verdict
    should_trade = Column(Boolean, default=False)

    # DE-06: Add indexes for frequently queried columns
    __table_args__ = (
        Index("idx_signal_timestamp", "timestamp"),
        Index("idx_signal_symbol", "symbol"),
    )


class TradeLog(Base):
    """Completed trades — the core of the learning engine."""
    __tablename__ = "trade_logs"

    id = Column(Integer, primary_key=True, autoincrement=True)
    # DE-07: Add foreign key for referential integrity
    signal_log_id = Column(Integer, ForeignKey("signal_logs.id"))
    opened_at = Column(DateTime, default=lambda: datetime.now(timezone.utc))
    closed_at = Column(DateTime)
    symbol = Column(String(20), nullable=False)
    timeframe = Column(String(10))
    direction = Column(String(10))  # LONG or SHORT
    regime = Column(String(20))
    # Prices
    entry_price = Column(Float)
    stop_loss = Column(Float)
    take_profit = Column(Float)
    exit_price = Column(Float)
    position_size = Column(Float)
    # Results
    pnl = Column(Float, default=0.0)
    pnl_pct = Column(Float, default=0.0)
    duration_seconds = Column(Integer, default=0)
    max_favorable = Column(Float, default=0.0)  # Best unrealized P&L
    max_adverse = Column(Float, default=0.0)     # Worst unrealized P&L
    exit_reason = Column(String(20))  # tp_hit, sl_hit, manual, time
    # Strategy context
    confluence_score = Column(Float, default=0.0)
    claude_confidence = Column(Integer, default=0)
    strategies_agreed = Column(Text)  # JSON list of strategy names that agreed
    # Learning
    outcome_grade = Column(String(1))  # A, B, C, D, F
    lessons_learned = Column(Text)     # Claude's post-trade analysis

    # DE-06: Indexes for frequently queried columns
    __table_args__ = (
        Index("idx_trade_opened_at", "opened_at"),
        Index("idx_trade_symbol", "symbol"),
        Index("idx_trade_symbol_opened", "symbol", "opened_at"),
        Index("idx_trade_exit_price", "exit_price"),  # DE-F06
    )


